package java2;

public class Piste {

    private int x;
    private int y;
    
    public Piste() {
        x = 0;
        y = 0;
    }
    public Piste(int x0, int y0) {
        x = x0;
        y = y0;
    }
    
    public int getX(){
        return x;
    }
    
    public int getY(){
        return y;
    }    
}
